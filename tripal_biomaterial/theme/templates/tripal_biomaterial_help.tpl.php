<h3> Biomaterial Content Type Description:</h3>
<p> The content type
    <b>Biomaterial</b> is one of the content types specified by the
    Tripal Expression module. The Tripal Expression module is built on top of the chado
    MAGE module. The biomaterial content type is used to detail a biomaterial. A biomaterial
    record is required for each library in the expression matrix file.</p>

<ul>
    <li>For more information on the chado biomaterial table see the Chado Mage module
        <a href="http://gmod.org/wiki/Chado_Mage_Module">GMOD wiki page</a>.
    </li>
    <li>For more informate on the MAGE schema see the FGED
        <a href="http://fged.org/projects/mage-tab/">MAGE-TAB page</a>.
    </li>
</ul>


