<h3> Analysis: Expression Content Type Description:</h3>
<p> The content type
    <b>Analysis: Expression</b> is one of the content types specified by the
    Tripal Expression module. The Tripal Expression module is built on top of the chado
    MAGE module. The Analysis: Expression content type is built on top of the Tripal Analysis
    module and is used to detail the design of a microarray or RNA-seq expression experiment.
</p>

<ul>
    <li>For more information on the chado analysis table see the Chado Mage module
        <a href="http://gmod.org/wiki/Chado_Mage_Module">GMOD wiki page</a>.
    </li>
    <li>For more informate on the MAGE schema see the FGED
        <a href="http://fged.org/projects/mage-tab/">MAGE-TAB page</a>.
    </li>
</ul>


